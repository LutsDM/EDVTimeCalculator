export type OrderFormAgbLine = { text: string; bold: boolean }

/** Zerlegt den AGB-Text in Zeilen; Überschriftenzeilen sind markiert. */
export function parseOrderFormAgbLines(
  body: string = ORDER_FORM_AGB_BODY
): OrderFormAgbLine[] {
  return body.replace(/\r\n/g, "\n").split("\n").map((line) => ({
    text: line,
    bold: ORDER_FORM_AGB_HEADINGS.has(line.trim()),
  }))
}

export type OrderFormAgbSection = {
  heading: string
  lines: OrderFormAgbLine[]
}

export function parseOrderFormAgbSections(
  body: string = ORDER_FORM_AGB_BODY
): OrderFormAgbSection[] {
  const lines = parseOrderFormAgbLines(body)

  const sections: OrderFormAgbSection[] = []
  let currentSection: OrderFormAgbSection | null = null

  for (const line of lines) {
    const trimmed = line.text.trim()

    if (line.bold && trimmed) {
      if (currentSection) {
        sections.push(currentSection)
      }

      currentSection = {
        heading: line.text,
        lines: [line],
      }
    } else {
      if (!currentSection) {
        currentSection = {
          heading: "",
          lines: [],
        }
      }

      currentSection.lines.push(line)
    }
  }

  if (currentSection) {
    sections.push(currentSection)
  }

  return sections
}

export function splitOrderFormAgbSectionsIntoColumns(
  body: string = ORDER_FORM_AGB_BODY
): [OrderFormAgbSection[], OrderFormAgbSection[]] {
  const sections = parseOrderFormAgbSections(body);

  const totalLines = sections.reduce(
    (sum, section) => sum + section.lines.length,
    0
  );

  const target = totalLines / 2;

  let current = 0;
  let splitIndex = sections.length;

  for (let i = 0; i < sections.length; i++) {
    const next = current + sections[i].lines.length;

    if (next >= target) {
      const diffBefore = Math.abs(target - current);
      const diffAfter = Math.abs(target - next);

      splitIndex = diffAfter <= diffBefore ? i + 1 : i;
      break;
    }

    current = next;
  }

  return [sections.slice(0, splitIndex), sections.slice(splitIndex)];
}